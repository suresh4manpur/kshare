package com.honewell.hackathon;

import java.util.Date;


public class Device {
	
	public String strDeviceType;
	public String strDeviceID;
	public String strName;
	public Double dValue;
	public Date Date;
	
	
	public String getStrDeviceType() {
		return strDeviceType;
	}
	public void setStrDeviceType(String strDeviceType) {
		this.strDeviceType = strDeviceType;
	}
	public String getStrDeviceID() {
		return strDeviceID;
	}
	public void setStrDeviceID(String strDeviceID) {
		this.strDeviceID = strDeviceID;
	}
	public String getStrName() {
		return strName;
	}
	public void setStrName(String strName) {
		this.strName = strName;
	}
	public Double getdValue() {
		return dValue;
	}
	public void setdValue(Double dValue) {
		this.dValue = dValue;
	}
	public Date getDate() {
		return Date;
	}
	public void setDate(Date date) {
		Date = date;
	}
	

}
